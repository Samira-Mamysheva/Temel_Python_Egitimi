# ENCAPSULATİON  --> değişkenlerin ne zaman değiştirilip değiştirilemeyeceğini kontrol ediyoruz

class Telefon:

  def __init__(self, isim, fiyat):
    self._isim = isim
    self.fiyat = fiyat

  def bilgi_ver(self):
    print(f"{self._isim} in fiyatı: {self.fiyat}")

  def isim_degistir(self, isim):
    self._isim = isim

  def _gizli_metod(self):
    print("gizli method çalıştırıldı")

pixel = Telefon("Pixel XL", 500)

pixel.bilgi_ver()

pixel.isim = "Pixel XLL" # değiştiremedi

pixel.bilgi_ver()

pixel.isim_degistir("Pixel XLL")

pixel.bilgi_ver()

