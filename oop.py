from abc import ABC, abstractclassmethod

class Araba(ABC):

  @abstractclassmethod    # abstract amacı tüm araba markalarının uyması gereken kuralları belirlemek
  def max_hiz(self):      # herkes beygir ve maxı tanımlamak zorunda
    pass                  # pass zorunluluk çünkü bu fonklardan iş yapması beklenmez

  @abstractclassmethod
  def beygir_gucu(self):
    pass

class Ornek():

  def ornek(self):
    pass

ornek = Ornek()

class Mercedes(Araba):

  def __init__(self, isim):
    self.isim = isim

  def beygir_gucu(self):
    return 500

  def max_hiz(self):
    return 100

merc = Mercedes("mercedes c 200")

merc.beygir_gucu()

merc.max_hiz()



# special method or dunder methods --> yerleşik fonklarla(print,len) nasıl etkileşime gireceiğini belirler

class Meyve:

  def __init__(self, isim, kalori):
    self.isim = isim
    self.kalori = kalori

  def __str__(self):
    return f"{self.isim} ın kalorisi şudur: {self.kalori}"

  def __len__(self):
    return self.kalori

portakal = Meyve("portakal", 200)

print(portakal)

len(portakal)

uzum = Meyve("üzüm", 250)

len(uzum)

str(uzum)

