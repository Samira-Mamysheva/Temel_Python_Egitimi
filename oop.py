# INHERİTANCE

class Muzisyen:
  def __init__(self, isim, yas):     # init kullandık objenin özel kaydetmek için
    self.isim = isim
    self.yas = yas
    print("Müzisyen sınıfı oluşturuldu")

  def ornek1(self):              # gerek yok inite
    print("örnek 1")

  def ornek2(self):
    print("örnek 2")

beethoven = Muzisyen("Beethoven")

beethoven.ornek1()



class Ozel_Muzisyen(Muzisyen):   # "Muzisyen" in tüm özl lerini aktardık

  def __init__(self, isim, yas):
    Muzisyen.__init__(self, isim, yas)   # Muzisyen den özl geçmesi için gerekli bir kurulum
    print("Özel Müzisyen sınıfı oluşturuldu")

  #override
  def ornek1(self):
    print("örnek 1 çalıştırıldı ama özel müzisyen")

beethoven = Ozel_Muzisyen("Beethoven", 2)



beethoven.ornek2()

beethoven.ornek1()



